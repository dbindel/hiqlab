% eplot = plotelt2d(e)
%
% Flatten an element array for plotting

function eplot = plotelt2d(e)

% HiQLab
% Copyright (c): Regents of the University of California

if size(e,1) == 4

  eplot = e([1 3 4 2],:);

elseif size(e,1) == 9

  eplot = [e([1 4 5 2],:), ...
           e([2 5 6 3],:), ...
           e([4 7 8 5],:), ...
           e([5 8 9 6],:)];

elseif size(e,1) == 16

  eplot = [e([ 1  5  6  2],:), ...
           e([ 2  6  7  3],:), ...
           e([ 3  7  8  4],:), ...
           e([ 5  9 10  6],:), ...
           e([ 6 10 11  7],:), ...
           e([ 7 11 12  8],:), ...
           e([ 9 13 14 10],:), ...
           e([10 14 15 11],:), ...
           e([11 15 16 12],:)];

end

